from rest_framework import serializers
from contact.models import Contact

###########################################################################################
# Serializers                                                                             #
###########################################################################################
# User Contact Serializer.
class ContactSerializer(serializers.ModelSerializer):
    class Meta:
        model = Contact
        fields = ['id', 'name', 'email', 'phone', 'message', 'date']

